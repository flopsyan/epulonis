export function pickLang(value) {
  return LANGS.includes(value) ? value : DEFAULT_LANG;
}

// Reads the language from the raw Cookie header (no extra dependency).
export function langFromCookie(cookieHeader) {
  if (!cookieHeader) return DEFAULT_LANG;
  for (const part of cookieHeader.split(';')) {
    const idx = part.indexOf('=');
    if (idx === -1) continue;
    const key = part.slice(0, idx).trim();
    if (key === 'epulonis-lang') return pickLang(decodeURIComponent(part.slice(idx + 1).trim()));
  }
  return DEFAULT_LANG;
}

export function makeT(lang) {
  const table = dict[pickLang(lang)];
  const fallback = dict[DEFAULT_LANG];
  return (key) => (key in table ? table[key] : fallback[key] ?? key);
}

// Full string table for a language (used to hydrate the browser modules).
export function clientStrings(lang) {
  return dict[pickLang(lang)];
}
